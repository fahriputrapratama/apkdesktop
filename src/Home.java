    public static void viewDatabuku(String where) {
        
        try {
            DefaultTableModel m = (DefaultTableModel) tblbuku.getModel();
            m.getDataVector().removeAllElements();
            Connection K = connect.configDB();
            Statement S = K.createStatement();
            String Q = "SELECT * FROM buku "+where;
            ResultSet R = S.executeQuery(Q);
            int n = 1;
            while (R.next()) {                 
                String judul = R.getString("judul");
                String penulis = R.getString("penulis");
                String status = R.getString("status");
                Object[] data = {n, judul, penulis, status};
                m.addRow(data);
            }
            
            tblbuku.getColumnModel().getColumn(0).setMinWidth(0);
            tblbuku.getColumnModel().getColumn(0).setMaxWidth(0);
            
        } catch (Exception e) {
            //error handling
        }
}
