    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed

        int n = tblbuku.getSelectedRow();
if(n != -1){
    String judul = tblbuku.getValueAt(n, 1).toString();

    int pilihan = JOptionPane.showConfirmDialog(this,
        "Apakah Anda yakin untuk menghapus data buku ini?",
        "Hapus Data", JOptionPane.YES_NO_OPTION);
    
    if(pilihan == JOptionPane.YES_OPTION){ // Gunakan JOptionPane.YES_OPTION
        String Q = "DELETE FROM buku WHERE judul='"+judul+"'";
        try {
            Connection K = connect.configDB();
            PreparedStatement ps = K.prepareStatement(Q);
            ps.executeUpdate();
         
            DefaultTableModel model = (DefaultTableModel) tblbuku.getModel();
            model.setRowCount(0); // Hapus semua baris di tabel sebelum refresh

            viewDatabuku(""); // Refresh tampilan tabel setelah delete
            
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }
}
    }//GEN-LAST:event_DeleteActionPerformed

    private void txtSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSimpanActionPerformed
        String judul = txtjudul.getText();
        String penulis = txtPenulis.getText();
        String status = cmbStatus.getSelectedItem().toString();

        try {
            Connection K = connect.configDB(); // Pastikan koneksi ke database berhasil

            if (K == null) {
                JOptionPane.showMessageDialog(this, "Koneksi ke database gagal.");
                return; // Hentikan eksekusi jika koneksi gagal
            }

            String Q = "INSERT INTO buku (judul, penulis, status) VALUES (?, ?, ?)";
            PreparedStatement PS = K.prepareStatement(Q);

            PS.setString(1, judul);
            PS.setString(2, penulis);
            PS.setString(3, status);

            PS.executeUpdate();

            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            
            txtjudul.setText("");
            txtPenulis.setText("");
            cmbStatus.setSelectedIndex(0);
            // Memanggil metode untuk memperbarui data di tabel jika sudah diimplementasikan
            viewDatabuku("");
            txtPenulis.requestFocus();

        } catch (Exception e) {

        }
    }//GEN-LAST:event_txtSimpanActionPerformed

